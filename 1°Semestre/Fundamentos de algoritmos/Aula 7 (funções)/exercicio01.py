# Exercício 1
# Crie uma função que calcule a média de dois números
#  ▶ Sua função deve receber dois números como parâmetros de entrada
#  ▶ Deve imprimir o resultado da média

def media (x,y):
    resultado = (x + y)/2
    print(f"A média de {x} e {y} é {resultado:.2f}")
(media(10,20))
(media(2.3,2.4))
